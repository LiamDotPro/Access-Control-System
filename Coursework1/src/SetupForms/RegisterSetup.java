    private void addRolesBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addRolesBtnActionPerformed

        int Index1 = rolesCombo.getSelectedIndex();

        ArrayList Types = new ArrayList();

        for (PersonType p : PersonType.values()) {
            Types.add(p.toString());
        }

        if (!roleList.contains(Types.get(Index1))) {
            roleList.addElement(Types.get(Index1));
        }


    }//GEN-LAST:event_addRolesBtnActionPerformed

