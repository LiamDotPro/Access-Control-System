    private void saveRoleBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveRoleBtnActionPerformed
        if (this.roleList.size() > 0) {

            try {
                InformationStructPeople tempInfoStruct = new InformationStructPeople();

                tempInfoStruct.setName(nameTxt.getText());

                List<PersonType> tempPersonList = new ArrayList<>();

                for (Object p : roleList.toArray()) {

                    switch (p.toString()) {
                        case "STUDENT":
                            tempPersonList.add(PersonType.STUDENT);
                            break;
                        case "CLEANER":
                            tempPersonList.add(PersonType.CLEANER);
                            break;
                        case "STAFFMEMBER":
                            tempPersonList.add(PersonType.STAFFMEMBER);
                            break;
                        case "VISITOR":
                            tempPersonList.add(PersonType.VISITOR);
                            break;
                        case "MANAGER":
                            tempPersonList.add(PersonType.MANAGER);
                            break;
                        case "SECURITY":
                            tempPersonList.add(PersonType.SECURITY);
                            break;
                        case "EMERGENCYRESPONDER":
                            tempPersonList.add(PersonType.EMERGENCYRESPONDER);
                            break;
                    }
                }
                tempInfoStruct.setRoles(tempPersonList);

                Controller.getSystemRegister().addPerson(tempInfoStruct);
                this.dispose();
            } catch (IndexOutOfBoundsException e) {
                JOptionPane.showMessageDialog(null, "You must enter at least one role", "No user Types Added", JOptionPane.WARNING_MESSAGE);
            }

        }
        else{
            JOptionPane.showMessageDialog(null, "You must enter at least one role", "No user Types Added", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_saveRoleBtnActionPerformed

